#include <unistd.h>
#include <stdio.h>
#include <string.h>
#include <limits.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	i = -1;
	argc = 0;
	while (argv[0][++i] != '\0')
		ft_putchar(argv[0][i]);
	ft_putchar('\n');
}
