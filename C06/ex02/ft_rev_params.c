#include <unistd.h>
#include <stdio.h>
#include <string.h>
#include <limits.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	while (--argc > 0)
	{
		i = -1;
		while (argv[argc][++i] != '\0')
			ft_putchar(argv[argc][i]);
		ft_putchar('\n');
	}
}
